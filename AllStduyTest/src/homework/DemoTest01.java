public class DemoTest01 {
    public static void main(String[] args) {
        // &  1
        System.out.println(7 & 1);
        // |   7
        System.out.println(7 | 1);
        // ~   -8
        System.out.println(~7);
        // ^   -8
        System.out.println(-1 ^7);
        // <<  6
        System.out.println(3 << 1);
        // >>
        System.out.println(3 >> 1);

    }
}
